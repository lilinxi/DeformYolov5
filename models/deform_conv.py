import torch.nn as nn
from models.common import autopad
import models.deform_conv_utils
import models.equi_conv_utils
import models.stereo_conv_utils


# torchvision implement for deformable conv v1
class DeformConvV1_TorchVision(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = models.deform_conv_utils.DeformConvV1_TorchVision(c1, c2, k, s, autopad(k, p), groups=g, bias=False)
        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def forward_fuse(self, x):
        return self.act(self.conv(x))


